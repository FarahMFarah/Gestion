package P1;

public class Matiere {
	

private int code;
private String nom;
private int coef;



public Matiere(int code, String nom, int coef) {
	super();
	this.code = code;
	this.nom = nom;
	this.coef = coef;
}

public int getCode() {
	return code;
}

public String getNom() {
	return nom;
}

public int getCoef() {
	return coef;
}

	

}
